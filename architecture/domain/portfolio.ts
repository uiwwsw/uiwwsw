export type GithubRepo = typeof githubRepo;
export type Portfolio = {
	readonly fullName: string;
	readonly desc: string;
	readonly repositoryUrl?: string;
	readonly previewUrl?: string;
	readonly youtubeUrl?: string;
	readonly img?: string;
};
export function convertGithubReposToPortfolios(
	githubRepos: GithubRepo[],
	localPortFolios?: Portfolio[]
): Portfolio[] {
	return githubRepos.map((githubRepo) => {
		const portfolio: Portfolio = {
			fullName: githubRepo.full_name,
			desc: githubRepo.description,
			repositoryUrl: githubRepo.html_url,
		};
		const localPortFolio = localPortFolios?.find(
			(localPortFolio) => localPortFolio.fullName === githubRepo.full_name
		);
		if (localPortFolio)
			return {
				...portfolio,
				previewUrl: localPortFolio.previewUrl,
				youtubeUrl: localPortFolio.youtubeUrl,
				img: localPortFolio.img,
			};
		return portfolio;
	});
}
